    private void jtDataGridMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtDataGridMouseClicked
        DefaultTableModel modelo = (DefaultTableModel) jtDataGrid.getModel();
        txtCodigo.setText(modelo.getValueAt(jtDataGrid.getSelectedRow(),0)+"");
        txtNombre.setText(modelo.getValueAt(jtDataGrid.getSelectedRow(),1)+"");
        txtPrecio.setText(modelo.getValueAt(jtDataGrid.getSelectedRow(),2)+"");
        for (int i = 0; i < cbCateg.getItemCount();i++){
            if(cbCateg.getItemAt(i).substring(cbCateg.getItemAt(i).indexOf(" ")+1,
                    cbCateg.getItemAt(i).length()).equals(modelo.getValueAt(jtDataGrid.getSelectedRow(),3))){
                cbCateg.setSelectedIndex(i);
            }
        }
        txtStock.setText(modelo.getValueAt(jtDataGrid.getSelectedRow(),4)+"");
        txtCodigo.setEnabled(false);
    }//GEN-LAST:event_jtDataGridMouseClicked

