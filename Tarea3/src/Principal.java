    public void llenarCateg(){
        
        try{
            Sql sql = new Sql();
            sql.conectar();
            cbCateg.removeAllItems();
            
            ResultSet lista = sql.select("*", "categorias");
            while(lista.next()){
                cbCateg.addItem(lista.getString("codigo")+" "+lista.getString("nombre"));
            }
            System.out.println("Seleccion exitosa");
            sql.desconectar();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error al seleccionar \n"+ex,"Error",3);
        }
       
        
    }
    public void  mostrarTabla(){
        try{
            jtDataGrid.removeAll();
            String nombreCategoria = "";
            Sql sql = new Sql();
            sql.conectar();
            ResultSet datos = sql.select("*", "productos");
            
            DefaultTableModel model = (DefaultTableModel)jtDataGrid.getModel();
            model.setRowCount(0);
            while(datos.next()){
                String sentencia = "select nombre from categorias where codigo = (select max(cod_categoria) from productos where cod_categoria = '"+
                        datos.getString("cod_categoria")+"')";
                
                ResultSet nombres = sql.select(sentencia);
                while (nombres.next()){
                    nombreCategoria = nombres.getString("nombre");
                }
                Object data[] = {datos.getString("codigo_prod"),
                                datos.getString("nombre"),
                                datos.getInt("precio"),
                               nombreCategoria,
                                datos.getInt("stock") 
                                };
                model.addRow(data);
                
            }
            System.out.println("Datos insertados correctamente.");
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error al insertar datos \n"+ex,"Error",3);
        }
        
    }

