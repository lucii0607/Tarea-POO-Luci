    private void txtBuscarCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtBuscarCaretUpdate
        String buscar = txtBuscar.getText();
        if(buscar.isEmpty()){
            jtDataGrid.clearSelection();
        }else{
            for(int i = 0; i<jtDataGrid.getRowCount(); i++){
                if(jtDataGrid.getValueAt(i, 1).toString().contains(buscar)){
                    jtDataGrid.changeSelection(i, 1, false, false);
                }
            }
        }
    }//GEN-LAST:event_txtBuscarCaretUpdate

