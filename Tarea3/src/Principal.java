        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        int precio = Integer.parseInt(txtPrecio.getText());
        String categoria = cbCateg.getSelectedItem().toString();
        categoria = categoria.substring(0,categoria.indexOf(" "));
        
        int stock = Integer.parseInt(txtPrecio.getText());
        
        
        
        
        String insert="INSERT INTO productos (codigo_prod,nombre,precio,cod_categoria,stock) VALUES ('"+ codigo +"','"+ nombre+"',"+precio+",'"+categoria+"',"+stock+")";
        Sql sql = new Sql();
        sql.conectar();
        
        sql.sentence(insert);
        sql.desconectar();
        mostrarTabla();
        
