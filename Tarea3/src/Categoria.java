    public void llenarLista(){
        try{
            Sql sql = new Sql();
            sql.conectar();
            ResultSet datos = sql.select("*", "categorias");
            DefaultListModel model = new DefaultListModel();
            lstCategC.setModel(model);
            
            //model.setRowCount(0);
            while(datos.next()){
                model.addElement(datos.getString("codigo")+" "+ datos.getString("nombre"));
            }
            System.out.println("Seleccion exitosa");
            sql.desconectar();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error al seleccionar \n"+ex,"Error",3);
        }
    }

