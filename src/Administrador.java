    private void lstUsuariosAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstUsuariosAMouseClicked
        String item = lstUsuariosA.getSelectedValue();
        String usuario="",nombre="",apellido="",rut="";
        char sexo=' ';
        byte edad=0,rol=0;
        Sql sql = new Sql();
        sql.conectar();
        
        ResultSet datos = sql.where("usuarios", "usuario", item);
        try{
            while(datos.next()){
                usuario = datos.getString("usuario");
                nombre = datos.getString("nombre");
                apellido  = datos.getString("apellido");
                rut = datos.getString("rut");
                sexo = datos.getString("sexo").charAt(0);
                edad  = datos.getByte("edad");
                rol = datos.getByte("rol");
            }
            sql.desconectar();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Usuario no valido \n"+ex,"Error",3);
        }
        txtUsuarioA.setText(usuario);
        txtNombreA.setText(nombre);
        txtApellidoA.setText(apellido);
        txtRutA.setText(rut);
        if (sexo == 'M'){
            rbMascA.setSelected(true);
        }else{
            rbFemA.setSelected(true);
        }
        jsEdadA.setValue(edad);
        if(rol==0){
            chbAdminA.setSelected(true);
        }else{
            chbEmpleadoA.setSelected(true);
        }
    }//GEN-LAST:event_lstUsuariosAMouseClicked

